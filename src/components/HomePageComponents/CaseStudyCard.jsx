import React, { useEffect, useRef } from 'react';
import { gsap } from 'gsap';
import { ScrollTrigger } from 'gsap/ScrollTrigger';
import { Check } from 'lucide-react';

gsap.registerPlugin(ScrollTrigger);

const CaseStudySection = () => {
    const sectionRefs = useRef([]);

    useEffect(() => {
        sectionRefs.current.forEach((section, index) => {
            if (!section) return;

            const leftSide = section.querySelector('.gsap-left');
            const rightSide = section.querySelector('.gsap-right');
            const cards = section.querySelectorAll('.gsap-card');
            const logo = section.querySelector('.gsap-logo');

            // Set initial states
            gsap.set([leftSide, rightSide], { opacity: 0, x: index % 2 === 0 ? -50 : 50 });
            gsap.set(cards, { opacity: 0, y: 20 });

            const tl = gsap.timeline({
                scrollTrigger: {
                    trigger: section,
                    start: "top 80%",
                    toggleActions: "play none none reverse"
                }
            });

            tl.to([leftSide, rightSide], {
                opacity: 1,
                x: 0,
                duration: 0.8,
                ease: "power3.out",
                stagger: 0.2
            })
                .to(cards, {
                    opacity: 1,
                    y: 0,
                    duration: 0.5,
                    stagger: 0.1,
                    ease: "back.out(1.7)"
                }, "-=0.4");

            // Floating Logo Animation
            gsap.to(logo, {
                y: 15,
                duration: 2,
                repeat: -1,
                yoyo: true,
                ease: "sine.inOut"
            });
        });

        return () => ScrollTrigger.getAll().forEach(t => t.kill());
    }, [studies]);

    return (
        <div className="w-full bg-[#0a0a0a] py-24 overflow-hidden">
            {studies.map((study, index) => {
                const isEven = index % 2 === 0;

                return (
                    <div
                        key={study.id}
                        ref={el => sectionRefs.current[index] = el}
                        className={`w-full max-w-7xl mx-auto px-6 py-24 grid grid-cols-1 lg:grid-cols-2 gap-12 lg:gap-24 items-center ${index !== 0 ? 'border-t border-white/5' : ''
                            }`}
                    >
                        {/* CONTENT SIDE */}
                        <div className={`flex flex-col items-center lg:items-start text-center lg:text-left gsap-left ${!isEven ? 'lg:order-2' : ''
                            }`}>
                            {/* Logo Wrapper */}
                            <div className="gsap-logo mb-8 h-20 w-20 p-4 flex items-center justify-center backdrop-blur-sm">
                                <img
                                    src={study.logo}
                                    alt={study.company}
                                    className="w-full h-full object-contain gray-scale invert brightness-200"
                                />
                            </div>

                            <span className="text-zinc-500 font-bold tracking-[0.3em] text-xs uppercase mb-4">
                                {study.badge} • {study.location}
                            </span>
                            <h2 className="text-5xl md:text-7xl lg:text-6xl font-black text-white leading-tight tracking-wider uppercase">
                                {study.company}.
                            </h2>
                            <p className="text-lg md:text-xl text-zinc-200/70 mt-6 max-w-md font-light italic border-l-2 border-zinc-800 pl-6">
                                "{study.description}"
                            </p>

                            <div className="mt-10 flex items-center gap-4 bg-white/5 border border-white/10 px-6 py-4 rounded-2xl">
                                <div className="flex flex-col">
                                    <span className="text-3xl font-black text-white">{study.results.revenueGrowth.split(' ')[0]}</span>
                                    <span className="text-[10px] uppercase tracking-widest text-zinc-500 font-bold">Monthly Opps</span>
                                </div>
                                <div className="w-[1px] h-10 bg-white/10" />
                                <div className="flex flex-col">
                                    <span className="text-3xl font-black text-white">{study.results.leadGen.split('+')[0]}+</span>
                                    <span className="text-[10px] uppercase tracking-widest text-zinc-500 font-bold">Monthly Leads</span>
                                </div>
                            </div>
                        </div>

                        {/* STRATEGY SIDE */}
                        <div className={`flex flex-col gap-4 w-full gsap-right ${!isEven ? 'lg:order-1' : ''
                            }`}>
                            {study.solution.map((step, i) => (
                                <div
                                    key={i}
                                    className="gsap-card group flex flex-col sm:flex-row items-center sm:items-start gap-4 p-6 bg-white/5 backdrop-blur-md border border-white/10 rounded-2xl md:rounded-[2rem] transition-all duration-300 hover:bg-white/10 hover:border-white/30"
                                >
                                    <div className="shrink-0 w-6 h-6 rounded-xl bg-zinc-400 flex items-center justify-center group-hover:bg-white transition-colors">
                                        <Check className="w-4 h-4 text-zinc-900" strokeWidth={3} />
                                    </div>
                                    <div>
                                        <h3 className="text-xl font-bold text-white mb-1">
                                            {i === 0 ? "The Scale" : i === 1 ? "The Build" : "The Result"}
                                        </h3>
                                        <p className="text-base text-zinc-100/60 leading-relaxed font-light leading-snug">
                                            {step}
                                        </p>
                                    </div>
                                </div>
                            ))}
                        </div>
                    </div>
                );
            })}
        </div>
    );
};

export default CaseStudySection;